#inicialización de operadores a utilizar
numero1 = int(input("ingrese su primer numero: "))
numero2 = int(input("ingrese su segundo numero: "))

#operadores aritméticos
print(f"la suma de {numero1} mas {numero2} es igual a: {numero1 + numero2}")
print(f"la multiplicacion de {numero1} por {numero2} es igual a: {numero1 * numero2}")

#operadores relacionales
print(f"los dos numeros son iguales?: {numero1 == numero2}")
print(f"el primer numero es menor que el segundo numero?: {numero1 < numero2}")
print(f"el segundo numero es mayor o igual que el primer numero?: {numero2 >= numero1}")